public class Course {

    private int grade;
    private int timeAvailableToStudy;
    private int difficulty;
    private int TimeToStudy;

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }

    public int getTimeAvailableToStudy() {
        return timeAvailableToStudy;
    }

    public void setTimeAvailableToStudy(int timeAvailableToStudy) {
        this.timeAvailableToStudy = timeAvailableToStudy;
    }

    public int getDifficulty() {
        return difficulty;
    }

    public void setDifficulty(int difficulty) {
        this.difficulty = difficulty;
    }

    public int getTimeToStudy() {
        return TimeToStudy;
    }

    public void setTimeToStudy(int TimeToStudy) {
        this.TimeToStudy = TimeToStudy;
    }
}
